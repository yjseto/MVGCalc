from enum import Enum

class LengthUnits(Enum):
    METER           = (1,'meter')
    CENTIMETER      = (2,'centimeter')
    KILOMETER       = (3,'kilometer')
    MILIMETER       = (4,'milimeter')
    MICROMETER      = (5,'micrometer')
    NANOMETER       = (6,'nanometer')
    MILE            = (7,'mile')
    YARD            = (8,'yard')
    FOOT            = (9,'foot')
    INCH            = (10,'inch')